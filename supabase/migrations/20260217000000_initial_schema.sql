  entry_type TEXT NOT NULL CHECK (entry_type IN ('prune', 'repot', 'water', 'fertilize', 'wire', 'defoliate', 'style', 'photo', 'health', 'note')),
  content TEXT,
  images TEXT[] DEFAULT '{}',
  created_at TIMESTAMPTZ DEFAULT now()
);

