-- ============================================================
-- Species reference table
-- ============================================================
CREATE TABLE tf_species (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  scientific_name TEXT UNIQUE NOT NULL,
  common_name TEXT NOT NULL,
  family TEXT,
  care_calendar JSONB NOT NULL DEFAULT '{}',
  climate_zones INTEGER[] DEFAULT '{}',
  indoor_outdoor TEXT CHECK (indoor_outdoor IN ('indoor', 'outdoor', 'both')),
  difficulty TEXT CHECK (difficulty IN ('beginner', 'intermediate', 'advanced')),
  watering TEXT,
  light TEXT,
  notes TEXT,
  created_at TIMESTAMPTZ DEFAULT now()
);

-- ============================================================
-- User's tree collection
-- ============================================================
CREATE TABLE tf_trees (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name TEXT NOT NULL,
  species_id UUID REFERENCES tf_species(id),
  age_years INTEGER,
  acquired_date DATE,
  source TEXT,
  style TEXT,
  size_class TEXT CHECK (size_class IN ('mame', 'shohin', 'kifu', 'chuhin', 'dai')),
  pot_type TEXT,
  soil_mix TEXT,
  description TEXT,
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now()
);

-- ============================================================
-- Tree images
-- ============================================================
CREATE TABLE tf_tree_images (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  tree_id UUID REFERENCES tf_trees(id) ON DELETE CASCADE,
  url TEXT NOT NULL,
  caption TEXT,
  angle TEXT CHECK (angle IN ('front', 'back', 'left', 'right', 'top', 'detail', 'roots', 'canopy', 'full')),
  taken_at DATE,
  uploaded_at TIMESTAMPTZ DEFAULT now()
);

-- ============================================================
-- Journal / timeline entries
